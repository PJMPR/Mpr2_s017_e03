package dao;

import domain.model.Person;

import java.sql.Connection;
import java.sql.PreparedStatement;
import java.sql.ResultSet;
import java.sql.SQLException;

public class PersonRepository extends RepositoryBase {

    String insertSql = "INSERT INTO people(name, surname) VALUES (?,?)";
    String selectByIdSql = "SELECT * FROM people WHERE id=?";
    PreparedStatement insert;
    PreparedStatement selectById;

    public PersonRepository(Connection connection) {
        super(connection);
        try {
            insert = connection.prepareStatement(insertSql);
            selectById = connection.prepareStatement(selectByIdSql);
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }

    public void add(Person person) {

        try {
            insert.setString(1, person.getName());
            insert.setString(2, person.getSurname());
            insert.executeUpdate();
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }

    public Person get(int personId) {
        try {
            selectById.setInt(1, personId);
            ResultSet resultSet = selectById.executeQuery();
            while (resultSet.next()) {
                Person result = new Person();
                result.setId(resultSet.getInt("id"));
                result.setName(resultSet.getString("name"));
                result.setSurname(resultSet.getString("surname"));
                return result;
            }

        } catch (SQLException e) {
            e.printStackTrace();
            return null;
        }
        return null;
    }


    @Override
    protected String createTableSql() {
        return ""
                + "CREATE TABLE people("
                + "id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,"
                + "name varchar(20),"
                + "surname varchar(50)"
                + ")";
    }

    @Override
    protected String tableName() {
        return "people";
    }


}
