package dao;

import java.sql.Connection;
import java.sql.ResultSet;
import java.sql.SQLException;
import java.sql.Statement;

/**
 * Created by L on 05.11.2016.
 */
public class HistoryRepository {

    private Connection connection;

    private String createTableSql = ""
            + "CREATE TABLE history("
            + "id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY ,"
            + "date DATE,"
            + "amount DECIMAL,"
            + "rate DOUBLE,"
            + "WALLET_FROM_ID int,"
            + "WALLTET_TO_ID int,"
            + "OPERATION_ID int,"
            + "FOREIGN KEY (WALLET_FROM_ID) REFERENCES wallet(id),"
            + "FOREIGN KEY (WALLTET_TO_ID) REFERENCES wallet(id),"
            + "FOREIGN KEY (OPERATION_ID) REFERENCES operations(id)"
            + ")";

    private Statement createTable;

    public HistoryRepository(Connection connection) {
        this.connection = connection;
        try {
            createTable = connection.createStatement();
            boolean tableExists = false;
            ResultSet resultSet = connection.getMetaData().getTables(null, null, null, null);

            while (resultSet.next()) {
                if (resultSet.getString("Table_Name").equalsIgnoreCase("history")) {
                    tableExists = true;
                    break;
                }
            }
            if (!tableExists) {
                createTable.executeUpdate(createTableSql);
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }

    public Connection getConnection() {
        return connection;
    }
}

